export const majorArcana = tarotCards.filter((c) => c.arcana === 'major');
export const minorArcana = tarotCards.filter((c) => c.arcana === 'minor');

export function getCardById(id: number): TarotCard | undefined {
  return tarotCards.find((c) => c.id === id);
}

export function getCardsBySuit(suit: string): TarotCard[] {
  return tarotCards.filter((c) => c.suit === suit);
}

export function getRandomCard(): TarotCard {
  return tarotCards[Math.floor(Math.random() * tarotCards.length)];
}

